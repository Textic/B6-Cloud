-- Script de Inicializacion
CREATE TABLE USUARIOS (
    id NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    nombre VARCHAR2(100) NOT NULL,
    rut VARCHAR2(20) NOT NULL,
    correo VARCHAR2(100) NOT NULL
);

CREATE TABLE AUTORES (
    id NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    nombre VARCHAR2(100) NOT NULL,
    nacionalidad VARCHAR2(50)
);

CREATE TABLE LIBROS (
    id NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    titulo VARCHAR2(100) NOT NULL,
    id_autor NUMBER NOT NULL,
    CONSTRAINT fk_autor FOREIGN KEY (id_autor) REFERENCES AUTORES(id)
);

CREATE TABLE PRESTAMOS (
    id NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    id_usuario NUMBER NOT NULL,
    id_libro NUMBER NOT NULL,
    fecha_prestamo DATE DEFAULT SYSDATE,
    estado VARCHAR2(20) NOT NULL,
    CONSTRAINT fk_usuario_az FOREIGN KEY (id_usuario) REFERENCES USUARIOS(id)
);

COMMIT;
